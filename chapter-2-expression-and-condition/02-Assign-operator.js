let assignment = 10;

assignment += 5;  // same as assignment = assignment + 5
console.log("assignment is now = ", assignment)   

assignment -= 5;  // same as assignment = assignment - 5
console.log("assignment is now", assignment);

assignment *= 5;  // same as assignment = assignment * 5
console.log("assignment is now", assignment);

assignment /= 5;  // same as assignment = assignment / 5
console.log("assignment is now", assignment);
