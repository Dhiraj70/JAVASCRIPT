let x = 5;
let y = 6;

// agar dono condition true rhaga to answer true hoga wrna answer is false.
console.log(x<y && x==5);  // AND Operator.

 // agar ek condition bhi true hota h to output = true.
console.log(x>y || x==5);  // OR Operator


// Jo true ko false kar da aur false ko true kr da that is called "Not Operator".

console.log(!false)   // NOT Operator
console.log(!true);   // NOT Operator

